package javaBeginner;

public class methods {
	static void  wish() {
		System.out.println("good morning");
		
	}
	static void  wish(int a) {
		System.out.println("good morning A" + a);
		
	}
	static void  wish(float b) {
		System.out.println("good morning B" + b);
		
	}
	
	public static void main(String args[]) {
	 wish();
	 wish(10);
	 wish(5.2f);
	
	 
	 
	
		
	}
}
